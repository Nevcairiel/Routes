* Генерация маршрута использует алгоритм, известный как |cffffff78Ant Colony Optimization|r. Этот алгоритм основан на наблюдаемом поведении муравьев в реальной жизни и использует тысячи случайных чисел для симуляции муравьев и генерации маршрутов.

* Это означает, что генерация маршрута случайна, и оптимизация одного и того же начального маршрута каждый раз будет давать разные результаты. Вы можете многократно оптимизировать маршрут, чтобы найти лучшее решение, Routes не отбросит более короткое известное решение.

* Другая причина, по которой мы хотим, чтобы сгенерированный маршрут был случайным, заключается в том, что вы не хотите, чтобы у 1000 других людей был точно такой же маршрут, как у вас, верно? Стандартные данные узлов WowHead легко загружаются для трех аддонов сбора.

* В текущей реализации в Routes алгоритм ACO сильно урезан, чтобы сократить время выполнения. Решение задачи NP-полной в песочнице WoW с использованием языка сценариев Lua — не самое идеальное занятие.

* Мы прилагаем усилия, чтобы маршрут не пересекал сам себя, но иногда это происходит, и алгоритм не замечает этого. Если это случается, просто оптимизируйте маршрут снова.
