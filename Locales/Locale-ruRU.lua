|cffffd200
Я создал маршрут с богатой адамантиновой рудой. Когда я нахожу обычную адамантиновую руду в том же месте, GatherMate/Cartographer удаляет богатый узел и заменяет его обычным. Это удаляет узел из моего маршрута, так как он состоит только из богатых узлов. Что я могу сделать?
