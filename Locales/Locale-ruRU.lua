2. Вы можете указать Routes не вставлять/удалять узлы автоматически. Эта опция находится в корневых настройках Routes в конфигурационном экране.

|cffffd200
Можно ли сделать индикатор прогресса для фоновой оптимизации маршрута?
|r
Индикатор прогресса невозможен для процесса оптимизации, так как это нелинейный алгоритм. Он работает на основе "многопроходной" системы, каждый проход улучшает предыдущий, пока улучшение не станет слишком минимальным, и тогда он останавливается.

Это похоже на утилиту |cffffff78Дефрагментация диска Windows XP|r, и её индикатор прогресса бесполезен, потому что он показывает только процент каждого прохода, но не знает, сколько проходов потребуется — это может быть 3 прохода, а может быть 10. В версии |cffffff78Vista|r индикатор прогресса вообще убрали.

|cffffd200
Как Routes выполняет оптимизацию маршрута?
|r
Routes использует алгоритм, называемый |cffffff78Ant Colony Optimization|r (ACO), который является эвристическим/вероятностным методом расчета оптимальных графов, основанным на наблюдаемом поведении муравьев в реальной жизни.

Алгоритмы ACO используются для получения почти оптимальных решений задачи |cffffff78Проблема коммивояжёра|r (TSP). Для дополнительной информации обратитесь к Google или Википедии.

|cffffd200
Что делает опция "Дополнительная оптимизация"?
|r
По умолчанию мы используем только ACO вместе со стандартным алгоритмом |cffffff782-opt|r для оптимизации маршрутов. Включение "дополнительной оптимизации" заставляет Routes также использовать 2.5-opt, который является подмножеством 3-opt. 2-opt — это процесс обмена парами рёбер (A-B и C-D становятся A-C и B-D) для создания более коротких маршрутов.

|cffffd200
Какой алгоритм используется для кластеризации узлов?
|r
Мы используем алгоритм иерархической агломеративной кластеризации с жадным подходом, поэтому результат детерминирован.

|cffffd200
Я создал запретную зону, прикрепил её к маршруту и оптимизировал его. Мой маршрут всё ещё проходит через запретную зону. Почему?
|r
Не всегда возможно найти маршрут, который не проходит через запретную зону, или иногда это крайне нецелесообразно.

Пользователь может создать запретные зоны, которые разделяют карту на непроходимые секции и регионы, поэтому алгоритм просто старается избегать их, если это возможно.

